#include "libft.h"

int	ft_isspace(const char c)
{
	char	c_tmp;

	c_tmp = (char)c;
	if (c_tmp == '\t' || c_tmp == '\n' || c_tmp == '\r'
		|| c_tmp == '\v' || c_tmp == '\f' || c_tmp == ' ')
		return (1);
	return (0);
}
